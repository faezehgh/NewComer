
<?php
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "test";

// Create connection
$conn = mysqli_connect($servername, $username, $password, $dbname);

$sql = "DELETE FROM information WHERE id_information='" . $_GET["id"] . "'";
